#!/bin/bash
#
# 2022.01.23 Darek L


########################################################################################################
# Settings
########################################################################################################


RESULTS_DIR="./Test/results/"

DOTS="................................................................"
DOTS+="......................................................................"

iNum=`ls -l *.md | wc -l`
current=0


########################################################################################################
# Main
########################################################################################################


echo -e "\n"

for i in *.md; do
	(( current++ ))
	
	FILE="${RESULTS_DIR}`basename "$i"`"
	
	MSG="${current} of ${iNum} | Processing: diff \"./${i}\" \"${FILE}\""
	echo -e "${MSG}" "${DOTS:${#MSG}}" "\c"
	
	
	diff "./$i" "${FILE}"
	
	if [[ $? -eq 0 ]]; then
		echo -e "passed."
	else
		echo -e "\n"
	fi

done


echo -e "\n"
